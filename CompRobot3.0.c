void Intake()
{
	if(vexRT[Btn5U] == 1)
	{
		motor[Intake2] = 127;
		motor[Intake1] = 127;
	}
	else if(vexRT[Btn5D] == 1)
	{
		motor[Intake2] = -127;
		motor[Intake1] = -127;
	}
	else if(vexRT[Btn5UXmtr2] == 1)
	{
		motor[Intake2] = 127;
		motor[Intake1] = 127;
	}
	else if(vexRT[Btn5DXmtr2] == 1)
	{
		motor[Intake2] = -127;
		motor[Intake1] = -127;
	}
	else
	{
		motor[Intake2] = 0;
		motor[Intake1] = 0;
	}

	if(vexRT[Btn7L] == 1)
	{
		IntakeRun = 1;
	}
	else if(vexRT[Btn7R] == 1)
	{
		IntakeRun = 0;
	}

	if (IntakeRun == 1)
	{
		motor[Intake2] = 127;
		motor[Intake1] = 127;
	}
}

