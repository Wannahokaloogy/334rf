void Drive()//thresholds keep motor whine at bay
{
	if(abs(vexRT[Ch4]) > threshold)
		X1 = -vexRT[Ch4]/1.6;
	else
		X1 = 0;

	if(abs(vexRT[Ch1]) > threshold)
		X2 = -vexRT[Ch1]/1.6;
	else
		X2 = 0;

	if(abs(vexRT[Ch3]) > threshold)
		Y1 = -vexRT[Ch3]/1.6;
	else
		Y1 = 0;

	motor[FRD] = (Y1) - (X2) - (X1);
	motor[BRD] =  (Y1) - (X2) + (X1);
	motor[FLD] = (Y1) + (X2) + (X1);
	motor[BLD] =  (Y1) + (X2) - (X1);
}

void velocity()
{
	velosityL = SensorValue[RightSpeed];
	velosityR = SensorValue[LeftSpeed];
}

task Pid(){
	float kp = .01;
	float ki = 0.01;
	float kd = 0;

	float currentL;
	float currentR;

	float integralActiveZone = 20;
	float errorTl;
	float errorTr;
	float lastErrorL;
	float lastErrorR;
	float proportionL;
	float proportionR;
	float integralL;
	float integralR;
	float derivativeL;
	float derivativeR;

	while(true)
	{
		velocity();

		float errorL = powerTarget - velosityL;
		float errorR = powerTarget - velosityR;
		//////////////////////////
		if(errorL <integralActiveZone && errorL != 0)
			errorTl += errorL;
		else
			errorTl = 0;

		if(errorR<integralActiveZone && errorR != 0)
			errorTr += errorR;
		else
			errorTr = 0;
		///////////////////////
		if(errorTl > 50/ki)
			errorTl = 50/ki;

		if(errorTr > 50/ki)
			errorTr = 50/ki;
		////////////////////////////
		if(errorL == 0)
			derivativeL = 0;
		if(errorR == 0)
			derivativeR = 0;
		////////////////////////////
		proportionL =errorL*kp;
		proportionR =errorR*kp;

		integralL = errorTl*ki;
		integralR = errorTr*ki;

		derivativeL = (errorL - lastErrorL)*kd;
		derivativeR = (errorR - lastErrorR)*kd;

		lastErrorL= errorL;
		lastErrorR= errorR;
		/////////////////////////

		currentL = proportionL + integralL + derivativeL;
		currentR = proportionR + integralR + derivativeR;

		motor[LauncherLB] = motor[LauncherLF] = currentL;
		motor[LauncherRB] = motor[LauncherRF] = currentR;

		wait1Msec(20);
	}
}

